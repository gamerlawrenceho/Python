#########################匯入模組#########################
import time
import mcu 
#########################函式與類別定義#########################

#########################宣告與設定#########################
gpio = mcu.gpio()  # 建立GPIO物件
servo = mcu.servo(gpio.D8)  # 建立輪詢器物件

#########################主程式#########################
servo.angle(30)
time.sleep(1)
servo.angle(90)
time.sleep(1)